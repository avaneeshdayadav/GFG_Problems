class Solution{
    public:
    
    //Function to rotate an array by d elements in counter-clockwise direction. 
    void rotateArr(int arr[], int d, int n){
        // code here
        if(d >= n)
            d = d % n;
            
        int l = 0, h = n-1;
        revArr(arr, l, h);
        revArr(arr,0,n-d-1);
        revArr(arr,n-d,n-1);
    }
    
    void revArr(int arr[], int l, int h)
    {
        while(l <= h)
        {
            swap(arr[l],arr[h]);
            l++;
            h--;
        }
    }
};

